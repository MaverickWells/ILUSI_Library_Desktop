    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(instance == null)
        {
            if(getTableSelectedRow() != 0)
            {
                int pil = JOptionPane.showConfirmDialog(rootPane, "Apakah Yakin Akan Menghapus Data", 
                        "Konfirmasi", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);

                if(pil == 0)
                {
                    C.deletePemustaka(Integer.parseInt(tableShow.getValueAt(
                            tableShow.getSelectedRow(), 0).toString()));
                    setTableData();
                }
            }
            else
                JOptionPane.showMessageDialog(rootPane, 
                        "Silakan Pilih Data Pada Tabel Yang Ada");
        }
        else
            JOptionPane.showMessageDialog(rootPane, 
                    "Sedang Ada Jendela Ubah/Tambah Yang Aktif, Silkan Matikan Dulu");
    }//GEN-LAST:event_btnHapusActionPerformed

