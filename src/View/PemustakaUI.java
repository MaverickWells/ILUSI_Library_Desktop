    private void btnAktivasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAktivasiActionPerformed
        // TODO add your handling code here:
        if(! tableShow.getValueAt(tableShow.getSelectedRow(), 9).toString().equalsIgnoreCase("Mahasiswa UJDP"))
            JOptionPane.showMessageDialog(rootPane, "Aktivasi Hanya Untuk Mahasiswa UJDP");
        else if(tableShow.getValueAt(tableShow.getSelectedRow(), 9).toString().equalsIgnoreCase("Dosen UJDP"))
            JOptionPane.showMessageDialog(rootPane, "Aktivasi Tidak Berlaku Untuk Dosen UJDP");
        else if(tableShow.getValueAt(tableShow.getSelectedRow(), 10).toString().equalsIgnoreCase("Aktif"))
            JOptionPane.showMessageDialog(rootPane, "Pemustaka Sudah Berstatus Aktif");
        else if(tableShow.getValueAt(tableShow.getSelectedRow(), 10).toString().equalsIgnoreCase("Tidak Aktif"))
        {
            C.setStatusAktif(Integer.parseInt(tableShow.getValueAt(
                    tableShow.getSelectedRow(), 0).toString()));
            
            JOptionPane.showMessageDialog(rootPane, "Pemustaka Berhasil Diaktifkan");
            
            setTableData();
        }
    }//GEN-LAST:event_btnAktivasiActionPerformed

