    private void setForm()
    {        
        radioBtnGroup.add(radioBtnNoAnggota);
        radioBtnGroup.add(radioBtnNamaAnggota);
        
        this.setTitle("PENGELOLAAN KATAGORI PEMUSTAKA");
        
        setTable();
    }
    
    private void setTable()
    {
        for(int i = 0; i < tableShow.getColumnCount(); i++)
        {
            Class<?> column = tableShow.getColumnClass(i);
            tableShow.setDefaultEditor(column, null);
        }
        
        tableShow.getTableHeader().setReorderingAllowed(false);
        
        tableShow.setRowSelectionAllowed(false);
        tableShow.setColumnSelectionAllowed(false);
        tableShow.setCellSelectionEnabled(true);
        
        tableShow.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        
        tableShow.setDragEnabled(false);
        tableShow.setShowGrid(true);
        
        tableShow.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    public void setTableData()
    {
        tableShow.setModel(C.showPemustaka());
    }
    
    public int getTableSelectedRow()
    {
        return tableShow.getSelectedRow() + 1;
    }
    
