    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String mode = "", cari = txtCari.getText().toString().trim().toLowerCase();
        
        if(radioBtnGroup.getSelection() == null)
            JOptionPane.showMessageDialog(rootPane, 
                    "Silakan Pilih Salah Satu Kategori Pencarian Terlebih Dahulu");
        else if(cari.equalsIgnoreCase("") && radioBtnNoAnggota.isSelected())
            JOptionPane.showMessageDialog(rootPane, 
                    "Silakan Inputkan Data Yang Ingin Dicari Terlebih Dahulu");
        else if(radioBtnNamaAnggota.isSelected())
        {
            mode = "nama_pemustaka";
            tableShow.setModel(C.showCariPemustaka(mode, cari));
        }
        else if(radioBtnNoAnggota.isSelected())
        {
            mode = "no_anggota";
            tableShow.setModel(C.showCariPemustaka(mode, cari));
        }
    }//GEN-LAST:event_btnCariActionPerformed

