    private void btnDeaktivasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeaktivasiActionPerformed
        // TODO add your handling code here:
        if(tableShow.getValueAt(tableShow.getSelectedRow(), 9).toString().equalsIgnoreCase("Dosen UJDP"))
            JOptionPane.showMessageDialog(rootPane, "Deaktivasi Tidak Berlaku Untuk Dosen UJDP");
        else if(tableShow.getValueAt(tableShow.getSelectedRow(), 10).toString().equalsIgnoreCase("Aktif"))
        {
            SimpleDateFormat df = new SimpleDateFormat("dd/MM");
            Calendar tgl = Calendar.getInstance();
            String tglSkrg = df.format(tgl.getTime());
            
            if(tglSkrg.equalsIgnoreCase("31/12") || tglSkrg.equalsIgnoreCase("31/07"))
            {
                C.setDeaktivasi(Integer.parseInt(tableShow.getValueAt(
                        tableShow.getSelectedRow(), 0).toString()));

                JOptionPane.showMessageDialog(rootPane, "Pemustaka Berhasil Dideaktivasi");


                setTableData();
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Belum Tanggal 31 Desember atau 31 Juli");
        }
        else if(tableShow.getValueAt(tableShow.getSelectedRow(), 10).toString().equalsIgnoreCase("Tidak Aktif"))
            JOptionPane.showMessageDialog(rootPane, "Status Pemustaka Sudah Tidak Aktif");
    }//GEN-LAST:event_btnDeaktivasiActionPerformed

