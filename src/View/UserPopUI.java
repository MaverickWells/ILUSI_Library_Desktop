    ////////////////////////METHOD-METHOD//////////////////////////
    private void clear()
    {
        txtUsername.setText("");
        txtPassword.setText("");
        cmbRole.setSelectedIndex(-1);
    }
    
    private void setFormEdit()
    {   
        oldUsername = data[1];
        
        txtUsername.setText(data[1]);
        txtPassword.setText(data[2]);
        
        for(int i=0; i < cmbRole.getItemCount(); i++)
            if(cmbRole.getItemAt(i).toString().equalsIgnoreCase(data[3]))
                cmbRole.setSelectedIndex(i);
        
        cmbRole.setEnabled(false);
        
        btnClear.setVisible(false);
    }
    
    private void setForm()
    {
        if(mode.equalsIgnoreCase("edit"))
        {
            this.setTitle("UBAH DATA");
            
            setFormEdit();            
        }
        else this.setTitle("TAMBAH DATA");
    }
    
    private boolean checkEmpty()
    {       
        if(txtUsername.getText().trim().equalsIgnoreCase("") ||
           new String(txtPassword.getPassword()).equalsIgnoreCase("") ||
           cmbRole.getSelectedIndex() == -1)
            return true;
        
        else return false;
    }
    
    private void setCmbBoxData()
    {
        cmbRole.removeAllItems();
        
        ArrayList<String> data = c.cmbBoxRoleData();
        
        for(int i=0; i < data.size(); i++)
            cmbRole.addItem(data.get(i).toString());
        
        cmbRole.setSelectedIndex(-1);
    }
    
    private void afterSet()
    {
        userUI.setInstance();
        userUI.setTableData();
        
        this.dispose();
    }
