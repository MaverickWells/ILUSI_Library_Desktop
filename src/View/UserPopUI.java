    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(mode.equalsIgnoreCase("add"))
        {
            if(checkEmpty())
                JOptionPane.showMessageDialog(rootPane, "Silakan Isi Data Yang Masih Kosong");
            else if(! c.isUniqueUsername(txtUsername.getText()))
                JOptionPane.showMessageDialog(rootPane, "Username Sudah Ada,"
                            + " Silakan Isi Dengan Yang Lain");
            else if(cmbRole.getSelectedItem().toString().substring(0, 7).equalsIgnoreCase("kaprodi"))
                if(! c.checkKaprodi(cmbRole.getSelectedItem().toString()))
                    JOptionPane.showMessageDialog(rootPane, "Kaprodi Sudah Ada, Silahkan Pilih Yang Lain");
            else
            {
                c.insertUser(new User(String.valueOf(c.getNewIDUser()), txtUsername.getText(),
                        new String(txtPassword.getPassword()), 
                        c.getIdRole(cmbRole.getSelectedItem().toString())));

                JOptionPane.showMessageDialog(rootPane, "Data Berhasil Dimasukkan");

                afterSet();
            }
        }
        else if(mode.equalsIgnoreCase("edit"))
        {
            if(checkEmpty())
                JOptionPane.showMessageDialog(rootPane, "Silakan Isi Data Yang Masih Kosong");
            else if(txtUsername.getText().equalsIgnoreCase(oldUsername))
            {
                c.editDataUser(new User(data[0], oldUsername, 
                        new String(txtPassword.getPassword()), 
                        c.getIdRole(cmbRole.getSelectedItem().toString())));
                
                JOptionPane.showMessageDialog(rootPane, "Data Berhasil Dimasukkan");

                afterSet();
            }
            else
            {
                if(c.isUniqueUsername(txtUsername.getText()))
                {
                    c.editDataUser(new User(data[0], txtUsername.getText(), 
                        new String(txtPassword.getPassword()), 
                        c.getIdRole(cmbRole.getSelectedItem().toString())));
                
                    JOptionPane.showMessageDialog(rootPane, "Data Berhasil Dimasukkan");

                    afterSet();
                }
                else
                    JOptionPane.showMessageDialog(rootPane, "Username Sudah Ada,"
                            + " Silakan Isi Dengan Yang Lain");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

