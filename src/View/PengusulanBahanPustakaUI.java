    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(tableShow.getRowCount() == 0)
            JOptionPane.showMessageDialog(rootPane, 
                    "Data Masih Kosong, Tidak Ada Yang Bisa Dihapus");
        else if(tableShow.getSelectedRow() != -1)
        {
            tableModel.removeRow(tableShow.getSelectedRow());
        }
        else
            JOptionPane.showMessageDialog(rootPane, 
                    "Silahkan Pilih Salah Satu Data Dari Tabel Terlebih Dahulu");
    }//GEN-LAST:event_btnHapusActionPerformed

