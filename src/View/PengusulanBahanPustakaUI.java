    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(instance != null)
            JOptionPane.showMessageDialog(rootPane, "Silahkan Selesaikan Entri "
                    + "atau Edit Usulan Bahan Pustaka Pada Form Yang Aktif");
        else if(tableShow.getRowCount() != 0)
        {
            int pil = JOptionPane.showConfirmDialog(rootPane,
                    "Apakah Anda Yakin Untuk Menyimpan Usulan Bahan Pustaka ?", 
                    "Konfirmasi Penyimpanan", JOptionPane.YES_NO_OPTION);

            if(pil == 0)
            {
                C.reservedUsulan((IdUsulan = C.getNewIDUsulan()), Integer.parseInt(noAnggota), 
                        labelTanggal.getText().trim());

                for(int i = 0; i < tableModel.getRowCount(); i++)
                {
                    C.insertNewDetilUsulan(IdUsulan, tableModel.getValueAt(i, 0).toString(), 
                            tableModel.getValueAt(i, 1).toString(), 
                            tableModel.getValueAt(i, 2).toString(), 
                            tableModel.getValueAt(i, 3).toString(), IdProdi);
                }

                JOptionPane.showMessageDialog(rootPane, "Usulan Anda Berhasil Disimpan");

                this.dispose();
            }
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Data Usulan Masih Kosong");
    }//GEN-LAST:event_btnSimpanActionPerformed

