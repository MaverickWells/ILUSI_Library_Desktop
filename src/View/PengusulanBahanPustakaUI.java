    public void addDataTableModel(String noISBN, String judul, String penerbit,
                                  String pengarang)
    {
        tableModel.addRow(new Object[]{noISBN, judul, 
                                       penerbit, pengarang});
    }
    
    public void editDataTableModel(int row, String noISBN, String judul, 
                                   String penerbit, String pengarang)
    {
        String[] dataTemp = {noISBN, judul, penerbit, pengarang};
        
        for(int i = 0; i < tableModel.getColumnCount(); i++)
            tableModel.setValueAt(dataTemp[i], row, i);
    }
