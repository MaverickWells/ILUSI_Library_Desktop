    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        if(instance == null)
        {
            if(tableShow.getSelectedRow() != -1)
            {
                ArrayList<String> data = new ArrayList<>();

                for(int i = 0; i < tableModel.getColumnCount(); i++)
                {
                    data.add(tableModel.getValueAt(tableShow.getSelectedRow(), i).
                            toString());
                }

                (instance = new PengusulanBahanPustakaPopUI(this, "edit", data)).
                        setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(rootPane, 
                    "Silahkan Pilih Data Yang Ingin Diubah");
        }
        else
            JOptionPane.showMessageDialog(rootPane, 
                    "Maaf, Editor Hanya Bisa Satu Dalam Satu Waktu");
    }//GEN-LAST:event_btnUbahActionPerformed

