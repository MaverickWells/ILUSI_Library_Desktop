    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(check())
        {

            int pil = JOptionPane.showConfirmDialog(rootPane,
                "Apakah Anda Yakin Untuk Mengganti Password Anda?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if(pil == 0)
            {
                if(C.updatePasswordPemustaka(username, String.valueOf(txtNewPass.getPassword())))
                {
                    JOptionPane.showMessageDialog(rootPane, "Password Anda Berhasil Diubah");

                    //homeUI.setInstance();

                    this.dispose();
                }
                else
                JOptionPane.showMessageDialog(rootPane, "Password Anda Tidak Berhasil Diubah");
            }
            else if(pil == 1)
            {
                JOptionPane.showMessageDialog(rootPane, "Silakan Mengubah Password Anda");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

