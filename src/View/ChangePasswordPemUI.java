    private boolean check()
    {
        if(String.valueOf(txtNewPass.getPassword()).equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Silakan Isi Password Baru");
            
            return false;
        }
        else if(String.valueOf(txtNewPassConf.getPassword()).equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Silakan Isi Konfirmasi Password");
            
            return false;
        }
        else if(! String.valueOf(txtNewPass.getPassword()).equalsIgnoreCase(
                String.valueOf(txtNewPassConf.getPassword())))
        {
            JOptionPane.showMessageDialog(rootPane, "Password Baru dan Konfirmasi Password Harus Sama");
            
            return false;
        }
        else return true;
    }
    
