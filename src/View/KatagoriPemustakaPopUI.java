    private void setFormEdit()
    {
        txtKode.setText(data[0]); txtKode.setEnabled(false);
        txtDesc.setText(data[1]);
        txtMasa.setText(data[2]);
        txtLama.setText(data[3]);
        
        btnClear.setVisible(false);        
    }
    
    private boolean checkEmpty()
    {
        if(txtKode.getText().toString().equalsIgnoreCase("") ||
           txtDesc.getText().toString().equalsIgnoreCase("") ||
           txtMasa.getText().toString().equalsIgnoreCase("") ||
           txtLama.getText().toString().equalsIgnoreCase(""))
            return true;
        else return false;
    }
    
    private void afterSet()
    {
        katPemUI.setTableData();
        katPemUI.setInstance();
        
        this.dispose();
    }
