    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        boolean status = false;
        
        Pattern pattern = Pattern.compile("[0-9]+");
        
        String kode = txtKode.getText().toString().trim();
        String desc = txtDesc.getText().toString().trim();
        String masa = txtMasa.getText().toString().trim();
        String lama = txtLama.getText().toString().trim();
        
        try
        {
            if(checkEmpty())
                throw new Exception("Lengkapi Data Yang Masih Kosong");
            else if(! pattern.matcher(kode).matches())
                throw new Exception("Kode Hanya Boleh Angka");
            else if(kode.length() > 2)
                throw new Exception("Panjang Karakter Kode Hanya Boleh 2");
            else if(! c.isUniqueKodeKatPem(kode) && mode.equalsIgnoreCase("add"))
                throw new Exception("Kode Katagori : " + kode + " Sudah Ada");
            else if(desc.length() > 50)
                throw new Exception("Panjang Karakter Deskripsi Katagori Hanya Boleh 50");
            else if(masa.length() > 50)
                throw new Exception("Panjang Karakter Masa Keanggotaan Hanya Boleh 50");
            else if(! pattern.matcher(lama).matches())
                throw new Exception("Lama Peminjaman Hanya Boleh Angka");
            else if(lama.length() > 3)
                throw new Exception("Panjang Karakter Lama Peminjaman Hanya Boleh 3");
            else status = true;
                
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
            
        if(status)
        {
            if(mode.equalsIgnoreCase("add"))
                c.insertKatPem(new KatagoriPemustaka(kode, desc, masa, lama));
            else if(mode.equalsIgnoreCase("edit"))
                c.updateKatPem(new KatagoriPemustaka(data[0], desc, masa, lama));
            
            afterSet();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

