    private void setTanggalPinjamKembali()
    {
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/YYYY");
        
        /////tanggal pinjam/////
        Calendar tgl = Calendar.getInstance();
        ////////////////////////
        
        labelTanggalPinjam.setText(df.format(tgl.getTime()));
        
        //////penambahan tanggal untuk pengembalian/////
        int hari = C.getLamaPinjamPemustaka(Integer.valueOf(
                txtNoAnggota.getText().trim()));
        
        tgl.add(Calendar.DATE, hari);
        ////////////////////////////////////////////////
        
        labelTanggalKembali.setText(df.format(tgl.getTime()));
    }
    
    public void addDataTableModel(String noKatalog, String noISBN, String judul, 
            String penerbit, String pengarang)
    {
        tableModel.addRow(new Object[]{noKatalog, noISBN, judul, penerbit, 
            pengarang});
    }
