    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(instance == null)
        {
            if(tableData.getRowCount() != 0)
            {
                int pil = JOptionPane.showConfirmDialog(rootPane, 
                        "Simpan Daftar Bahan Pustaka Yang Dipinjam ?", 
                        "Konfirmasi Penyimpanan", JOptionPane.YES_NO_OPTION);

                if(pil == 0)
                {
                    int id_transaksi = C.getNewIDTransaksi();

                    C.insertNewTransaksi(id_transaksi, Integer.valueOf(
                            txtNoAnggota.getText().trim()), labelTanggalPinjam.getText(), 
                            labelTanggalKembali.getText());

                    for(int i = 0; i < tableModel.getRowCount(); i++)
                    {
                        C.insertNewDetilTransaksi(id_transaksi, 
                                tableModel.getValueAt(i, 0).toString());
                    }

                    JOptionPane.showMessageDialog(rootPane, 
                            "Data Peminjaman Berhasil Dimasukkan");

                    this.dispose();
                }
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Silahkan Masukkan Bahan "
                        + "Pustaka Yang Ingin Dipinjam Terlebih Dahulu");
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Masih Ada Jendela Tambah Yang Aktif");
    }//GEN-LAST:event_btnSimpanActionPerformed

