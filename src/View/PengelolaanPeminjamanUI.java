    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(instance == null)
        {
            if(tableData.getSelectedRow() != -1)
            {
                int pil = JOptionPane.showConfirmDialog(rootPane, 
                        "Hapus Bahan Pustaka ?", "Konfirmasi Penghapusan", 
                        JOptionPane.YES_NO_OPTION);

                if(pil == 0)
                {
                    C.KembaliBahanPustaka(tableData.getValueAt(
                            tableData.getSelectedRow(), 0).toString());

                    tableModel.removeRow(tableData.getSelectedRow());
                }
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Silahkan Pilih Data Yang Ingin Dihapus");
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Masih Ada Jendela Tambah Yang Aktif");
    }//GEN-LAST:event_btnHapusActionPerformed

