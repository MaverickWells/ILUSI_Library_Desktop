    private void btnCekNoAnggotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekNoAnggotaActionPerformed
        // TODO add your handling code here:
        Pattern ptrn = Pattern.compile("[0-9]+");

        if(txtNoAnggota.getText().equalsIgnoreCase(""))
            JOptionPane.showMessageDialog(rootPane, "Silakan Masukkan No. Anggota");
        else if(! ptrn.matcher(txtNoAnggota.getText().trim()).matches())
            JOptionPane.showMessageDialog(rootPane, "No. Anggota Hanya Angka");
        else
        {
            if(C.cekPemustaka(Integer.valueOf(txtNoAnggota.getText().trim())).
                    equalsIgnoreCase("Tidak Aktif") || 
               C.cekPemustaka(Integer.valueOf(txtNoAnggota.getText().trim())).
                       equalsIgnoreCase("Aktif"))
            {
                JOptionPane.showMessageDialog(rootPane, "No. Anggota Terdaftar."
                    + "\nSilahkan Memproses Pengembalian Bahan Pustaka");

                setInputPengembalianOn();
                setTanggalKembali();

                btnCekNoAnggota.setEnabled(false);

                txtNoAnggota.setEditable(false);

                tableData.setModel(C.getDataPeminjaman(Integer.valueOf(
                    txtNoAnggota.getText().trim())));
            }
            else
            JOptionPane.showMessageDialog(rootPane, "No. Anggota Tidak Ditemukan."
                + " \nSilakan Masukkan Ulang No. Anggota");
        }
    }//GEN-LAST:event_btnCekNoAnggotaActionPerformed

