    private void btnPengembalianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPengembalianActionPerformed
        // TODO add your handling code here:
        if(tableData.getSelectedRow() != -1)
        {
            int pil = JOptionPane.showConfirmDialog(rootPane,
                "Konfirmasi Pengembalian Bahan Pustaka Ini",
                "Konfirmasi Pengembalian", JOptionPane.YES_NO_OPTION);

            if(pil == 0)
            {
                DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                DateFormat df2 = new SimpleDateFormat("yyyy-MM-dd");
                Date tglKembali = null, tglKemAktual = null;
                
                try {
                    tglKembali = df2.parse(C.getTanggalKembali(Integer.valueOf(
                            txtNoAnggota.getText().trim()),
                            tableData.getValueAt(tableData.getSelectedRow(), 1).toString()));
                    
                    tglKemAktual = df.parse(labelTanggalKembaliAktual.getText());
                }
                catch (ParseException ex) {
                    Logger.getLogger(PengelolaanPengembalianUI.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                no_detil.add(tableData.getValueAt(tableData.getSelectedRow(), 0).toString());
                
                if(tglKemAktual.after(tglKembali))
                {
                    int diff = (int) (tglKemAktual.getTime() - tglKembali.getTime())/
                            (24 * 60 * 60 * 1000);
                    
                    labelDenda.setText("Rp. " + String.valueOf(total_denda += diff * 500));
                    
                    C.KembaliBahanPustaka(tableData.getValueAt(
                        tableData.getSelectedRow(), 1).toString());
                    
                    C.pengembalianBahanPustakaDenda(Integer.valueOf(
                            tableData.getValueAt(tableData.getSelectedRow(), 0).toString()),
                            labelTanggalKembaliAktual.getText(), diff * 500);
                }
                else
                {
                    C.KembaliBahanPustaka(tableData.getValueAt(
                        tableData.getSelectedRow(), 1).toString());
                    
                    C.pengembalianBahanPustaka(Integer.valueOf(
                            tableData.getValueAt(tableData.getSelectedRow(), 0).toString()),
                        labelTanggalKembaliAktual.getText());
                }
                
                JOptionPane.showMessageDialog(rootPane, "Bahan Pustaka Berhasil Dikembalikan");

                tableData.setModel(C.getDataPeminjaman(Integer.valueOf(
                    txtNoAnggota.getText().trim())));
            }
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Silahkan Pilih Bahan Pustaka "
                + "Yang Akan Dikembalikan Dari Tabel");
    }//GEN-LAST:event_btnPengembalianActionPerformed

